    private void updateLista(String sql) {
        lista = daoQuadrinho.query(sql);
        DefaultTableModel modelo = (DefaultTableModel) jTableQuadrinho.getModel();
        modelo.setRowCount(0);

        for (Quadrinho q : lista) {
            modelo.addRow(new Object[]{
                q.getId(),
                q.getNome(),
                String.format("R$ %, .2f", q.getValor()),
                q.isDisponibilidade() ? "disponível" : "indisponível"
            });
        }

        cleanForms();
    }

