    private void jTableQuadrinhoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableQuadrinhoMouseClicked
        Quadrinho quadrinho = lista.get(jTableQuadrinho.getSelectedRow());

        jTextNome2.setText(quadrinho.getNome());
        jTextValor.setText(String.format("R$ %,.2f", quadrinho.getValor()));
        jTextEditora.setText(quadrinho.getEditora());
        jTextISBN.setText(quadrinho.getIsbn());
        jTextEdicao.setText(quadrinho.getEdicao());

        if (quadrinho.isDisponibilidade()) {
            if (quadrinho.isVersaoFisica()) {
                jLabelFisica.setForeground(Color.GREEN);
            } else {
                jLabelFisica.setForeground(Color.GRAY);
            }
            if (quadrinho.isVersaoDigital()) {
                jLabelDigital.setForeground(Color.GREEN);
            } else {
                jLabelDigital.setForeground(Color.GRAY);
            }
        } else {
            jLabelFisica.setForeground(Color.GRAY);
            jLabelDigital.setForeground(Color.GRAY);
        }

        jTextGenero.setText(quadrinho.getGenero());
        jTextNota.setText(String.format("%,.2f", quadrinho.getNota()));
        jTextRecomend.setText(String.format("%s", quadrinho.isRecomendavel() ? "Sim" : "não"));
    }//GEN-LAST:event_jTableQuadrinhoMouseClicked

    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked
        double total = 0;
        lista = daoQuadrinho.query("SELECT * FROM quadrinho");
        for (Quadrinho q : lista) {
            total += q.getValor();
        }
        JOptionPane.showMessageDialog(null, "Total gasto na aquisição de todos os quadrinhos\n" + String.format("R$ %, .2f", total), "Custos", JOptionPane.INFORMATION_MESSAGE);
    }//GEN-LAST:event_jButton1MouseClicked

