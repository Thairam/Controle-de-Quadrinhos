    private void jTableEmprestimoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableEmprestimoMouseClicked
        Emprestimo emprestimo = listaEmprestimos.get(jTableEmprestimo.getSelectedRow());

        listaItens = daoItem
                .query("select DISTINCT * from item INNER JOIN quadrinho ON item.id_quadrinho = quadrinho.id "
                        + "WHERE item.id_emprestimo = " + emprestimo.getId());

        tabelaQuadrinhos.setRowCount(0);

        for (Item item : listaItens) {
            Quadrinho q = item.getQuadrinho();
            tabelaQuadrinhos.addRow(new Object[]{q.getNome(), q.getValor(), q.getNota(),
                item.getVersao().equals("F") ? "Física" : "Digital"});
        }
    }//GEN-LAST:event_jTableEmprestimoMouseClicked

    private void jTableAmigoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableAmigoMouseClicked
        Amigo amigo = listaAmigos.get(jTableAmigo.getSelectedRow());
        listaEmprestimos = daoEmprestimo.query("SELECT DISTINCT * FROM emprestimo WHERE emprestimo.id_amigo = " + amigo.getId());

        tabelaQuadrinhos.setRowCount(0);
        tabelaEmprestimos.setRowCount(0);

        for (Emprestimo e : listaEmprestimos) {
            tabelaEmprestimos.
                    addRow(new Object[]{e.getId(),
                Utils.calendToString(e.getDataEmprestimo()),
                Utils.calendToString(e.getDataDevolucao()),
                e.getEstado()
            });
        }

    }//GEN-LAST:event_jTableAmigoMouseClicked

