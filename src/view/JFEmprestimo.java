    private void updateLista(String sql) {

        listaAmigos = daoAmigo.query(sql);
        tabelaAmigos.setRowCount(0);

        for (Amigo a : listaAmigos) {
            tabelaAmigos.addRow(new Object[]{a.getId(), a.getNome(), a.getCpf()});
        }

        tabelaQuadrinhos.setRowCount(0);
        tabelaEmprestimos.setRowCount(0);
    }

