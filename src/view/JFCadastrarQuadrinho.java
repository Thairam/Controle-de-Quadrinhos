    public JFCadastrarQuadrinho(Quadrinho quadrinho) {
        initComponents();
        this.quadrinho = quadrinho;
        if (quadrinho.getId() != 0) {
            this.setTitle("Alterar dados");
            this.jBCadastrar.setText("Alterar");
            this.jTextNome.setText(quadrinho.getNome());
            this.jTextValor.setText(String.format("R$ %,.2f", quadrinho.getValor()));
            this.jTextEditora.setText(quadrinho.getEditora());
            this.jTextISBN.setText(quadrinho.getIsbn());
            this.jTextEdicao.setText(quadrinho.getEdicao());
            this.jTACuriosidade.setText(quadrinho.getCuriosidade());

            if (!quadrinho.isDisponibilidade()) {
                this.jRadioButtonF.setSelected(false);
                this.jRadioButtonD.setSelected(false);
            }

            lista = daoItem.query("SELECT DISTINCT item.id, item.versao, item.id_emprestimo, item.id_quadrinho from item inner join "
                    + "quadrinho ON item.id_quadrinho = " + quadrinho.getId() + " inner join emprestimo "
                    + "WHERE emprestimo.estado = 'ABERTO';");

            for (Item item : lista) {
                if (item.getVersao().equals("F")) {
                    jRadioButtonF.setEnabled(false);
                } else if (item.getVersao().equals("D")) {
                    jRadioButtonD.setEnabled(false);
                }
            }

            this.jTextGenero.setText(quadrinho.getGenero());
            if (quadrinho.isRecomendavel()) {
                jRadioButtonSim.setSelected(true);
            } else {
                jRadioButtonNao.setSelected(true);
            }
            this.jTextNota.setText(String.format("%,.1f", quadrinho.getNota()));
        } else {
            this.jTextNome.setText("");
            this.jTextValor.setText(String.format("%,.1f", quadrinho.getNota()));
            this.jTextEditora.setText("");
            this.jTextISBN.setText("");
            this.jTextEdicao.setText("");
            this.jRadioButtonD.setSelected(true);
            this.jRadioButtonF.setSelected(true);
            this.jTextGenero.setText("");
            this.jRadioButtonSim.setSelected(true);
            this.jTextNota.setText("");
        }
    }

