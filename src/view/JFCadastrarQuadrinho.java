    private void jBCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBCadastrarActionPerformed
        quadrinho.setColecao(null);
        quadrinho.setNome(jTextNome.getText());
        quadrinho.setValor(Utils.toDouble(jTextValor.getText()));
        quadrinho.setEditora(jTextEditora.getText());
        quadrinho.setIsbn(jTextISBN.getText());
        quadrinho.setEdicao(jTextEdicao.getText());
        quadrinho.setGenero(jTextGenero.getText());
        quadrinho.setCuriosidade(jTACuriosidade.getText());
        quadrinho.setNota(Utils.toDouble(jTextNota.getText()));

        if (jRadioButtonF.isSelected() || jRadioButtonD.isSelected()) {
            quadrinho.setDisponibilidade(true);
            if (jRadioButtonF.isSelected()) {
                quadrinho.setVersaoFisica(true);
            }
            if (jRadioButtonD.isSelected()) {
                quadrinho.setVersaoDigital(true);
            }
        } else {
            quadrinho.setDisponibilidade(false);
        }

        if (jRadioButtonSim.isSelected()) {
            quadrinho.setRecomendavel(true);
        } else {
            quadrinho.setRecomendavel(false);
        }

        if (jTextNome.getText().equals("") || jTextEditora.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "ERROR!\nFalta o preenchimento de\ndados obrigatório(s) do quadrinho!", "", JOptionPane.INFORMATION_MESSAGE);
        } else {

            if (quadrinho.getId() == 0) {
                daoQuadrinho.insert(quadrinho);
            } else {
                daoQuadrinho.update(quadrinho);
            }

            this.dispose();
        }

    }//GEN-LAST:event_jBCadastrarActionPerformed

